    private void createEnterpriseButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEnterpriseButton5ActionPerformed
        // TODO add your handling code here:
        
        Organization o = (Organization)cbOrganization1.getSelectedItem();
        for(Employee e:o.getEmployeeDirectory().getEmployeeList()){
            if(e.getUserName().equals(txtName2.getText())){
                JOptionPane.showMessageDialog(null, "Organization already exists", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Employee e = new Employee();
        e.setName(txtName1.getText());
        e.setUserName(txtName2.getText());
        e.setEmailId(txtName3.getText());
        e.setPassword(String.valueOf(jPasswordField1.getPassword()));
        o.getEmployeeDirectory().getEmployeeList().add(e);
        JOptionPane.showMessageDialog(null, "Employee added successfully "+ e.getUserName());
        populateEmployeeTable();
        
        txtName1.setText("");
        txtName2.setText("");
        txtName3.setText("");
        jPasswordField1.setText("");
    }//GEN-LAST:event_createEnterpriseButton5ActionPerformed

