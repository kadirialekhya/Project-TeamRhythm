    public void populateEnterpriseTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseTable.getModel();
        model.setRowCount(0);

        for (Network network : system.getNetworkList()) {
            tempNetwork = network;
            if (network.getUserAccountDirectory().getUserAccountList().contains(admin)) {
                for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                    Object[] row = new Object[2];
                    row[0] = e.getName();
                    row[1] = e.getEnterpriseType();
                    model.addRow(row);
                }
                break;
            }
        }
    }

    public void populateOrganizationTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseTable1.getModel();
        model.setRowCount(0);

        for (Enterprise e : tempNetwork.getEnterpriseDirectory().getEnterpriseList()) {
            for (Organization o : e.getOrganizationDirectory().getOrganizationList()) {
                Object[] row = new Object[2];
                row[1] = e.getName();
                row[0] = o;
                model.addRow(row);
            }
        }

    }

    public void populateEmployeeTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseTable2.getModel();
        model.setRowCount(0);

        for (Enterprise e : tempNetwork.getEnterpriseDirectory().getEnterpriseList()) {
            System.out.println("Ente"+e.getName());
            for (Organization o : e.getOrganizationDirectory().getOrganizationList()) {
                System.out.println("Org"+o.getName());
                for (Employee emp : o.getEmployeeDirectory().getEmployeeList()) {
                    Object[] row = new Object[4];
                    row[1] = e.getName();
                    row[0] = o.getName();
                    row[2] = emp;
                    row[3] = emp.getUserName();
                    model.addRow(row);
                }
            }
        }

    }


