    private void createEnterpriseButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEnterpriseButton3ActionPerformed
        // TODO add your handling code here:

        Enterprise e = (Enterprise) cbEnterprise.getSelectedItem();
        for (Organization o : e.getOrganizationDirectory().getOrganizationList()) {
            if (o.getName().equals(txtName.getText())) {
                JOptionPane.showMessageDialog(null, "Organization already exists", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        e.getOrganizationDirectory().createOrganization((Organization.Type) cbOrganization.getSelectedItem());
        JOptionPane.showMessageDialog(null, "Organization added successfully");
        populateOrganizationTable();
        
    }//GEN-LAST:event_createEnterpriseButton3ActionPerformed

